@Composable
fun TrailerlyTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable () -> Unit
) {
    val profileViewModel: ProfileViewModel = hiltViewModel()
    val vmDark by profileViewModel.darkMode.collectAsState()
    var isDarkMode by remember { mutableStateOf(vmDark ?: darkTheme) }

    LaunchedEffect(vmDark) {
        isDarkMode = vmDark ?: darkTheme
    }

    val colorScheme = if (isDarkMode) {
        DarkColorScheme
    } else {
        LightColorScheme
    }

    MaterialTheme(
        colorScheme = colorScheme,
        typography = Typography,
        content = content
    )
}

@Composable
fun TrailerlyTheme(content: @Composable () -> Unit) {
    TrailerlyTheme(darkTheme = false, content = content)
}
